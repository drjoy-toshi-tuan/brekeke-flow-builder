FILLERS_10 = ["あ", "あー", "あの", "え", "えー", "えっと", "ん", "はい", "ま", "そうですね"]  # まー不使用（教師データ準拠）
FILLERS_3 = ["あ", "え", "えー"]
SUFFIXES_4 = ["です", "で", "なんですが", "になります"]
SUFFIXES_2 = ["です", "で"]

def entry(h, y, fillers=None, suffixes=None, drops=1):
    """1キーワードのフルエントリ生成"""
    lines = [f"{h} {y}"]
    if fillers:
        for f in fillers:
            lines.append(f"{h} {f}{y}")
    if suffixes:
        for s in suffixes:
            lines.append(f"{h} {y}{s}")
    for i in range(1, min(drops + 1, len(y))):
        d = y[i:]
        if len(d) >= 2:
            lines.append(f"{h} {d}")
    return lines

def dedup(lines):
    return list(dict.fromkeys(lines))


