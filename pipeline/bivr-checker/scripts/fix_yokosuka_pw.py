def build_pw_shinryouka(include_nashi=False):
    lines = []
    # 正式名: フィラー3種 + 語尾4種 + 頭落ち2段
    for h, y in DEPARTMENTS:
        lines.extend(entry(h, y, FILLERS_3, SUFFIXES_4, 2))
    # 略称: 頭落ち1段のみ（フィラーなし — 略称は短いため認識しやすい）
    for h, y in DEPT_ALIASES:
        lines.extend(entry(h, y, None, None, 1))

    if include_nashi:
        for h, y in [("ありません","ありません"),("ないです","ないです"),("なし","なし"),
                     ("特にありません","とくにありません"),("特にない","とくにない"),
                     ("大丈夫です","だいじょうぶです"),("ございません","ございません"),
                     ("それだけです","それだけです"),("一つだけです","ひとつだけです")]:
            lines.extend(entry(h, y, FILLERS_3, None, 2))
    return dedup(lines)


