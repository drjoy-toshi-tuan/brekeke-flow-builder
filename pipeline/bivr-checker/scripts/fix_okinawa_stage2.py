FILLERS = ["あ", "あー", "あの", "えー", "えっと", "んー"]

def filler_entries(display, readings):
    """Generate filler-prefixed entries for a keyword.
    display: 表記 (e.g. "予約")
    readings: list of よみ (e.g. ["よやく"])
    """
    result = []
    for reading in readings:
        for f in FILLERS:
            result.append(f"{display} {f}{reading}")
    return result


