# Helper: build profile_words string from list of "表記 よみ"
# ============================================================
def pw_string(lines):
    """Join lines with actual newline characters. Filter blanks/comments."""
    cleaned = []
    for line in lines:
        line = line.strip()
        if not line or line.startswith("#"):
            continue
        cleaned.append(line)
    return "\n".join(cleaned)

def count_words(pw):
    if not pw:
        return 0
    return len([l for l in pw.split("\n") if l.strip()])


