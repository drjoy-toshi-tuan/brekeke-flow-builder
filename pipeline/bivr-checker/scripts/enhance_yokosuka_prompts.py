def load_json(path):
    with open(path, "r", encoding="utf-8") as f:
        return json.load(f)


def save_json(path, data):
    with open(path, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    print(f"  Saved: {path}")


def update_prompt(data, module_name, new_prompt):
    """Update params.prompt for a given module name"""
    if module_name in data["modules"]:
        mod = data["modules"][module_name]
        if "generate_by_OpenAI" in mod.get("type", ""):
            old = mod["params"].get("prompt", "")
            mod["params"]["prompt"] = new_prompt
            old_lines = len(old.split("\n")) if old else 0
            new_lines = len(new_prompt.split("\n"))
            print(f"  {module_name}: {old_lines} lines -> {new_lines} lines")
            return True
    print(f"  WARNING: {module_name} not found or not OpenAI type")
    return False


