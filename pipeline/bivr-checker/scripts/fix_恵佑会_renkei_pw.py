def lines(*entries):
    """Join list of 'word yomi' pairs (tuples or strings) into profile_words string."""
    result = []
    seen = set()
    for e in entries:
        if isinstance(e, tuple):
            line = f"{e[0]} {e[1]}"
        else:
            line = e
        if line not in seen:
            seen.add(line)
            result.append(line)
    return "\n".join(result) + "\n"


def with_fillers(word, yomi, fillers=None, suffixes=None):
    """Generate filler + word + suffix combinations."""
    if fillers is None:
        fillers = ["あ", "え", "えー", "あの", "はい", "ま"]
    if suffixes is None:
        suffixes = ["です", "で", "なんですが", "になります", "ね", "さ", "でして", "か"]

    result = [(word, yomi)]
    # filler + word
    for f in fillers:
        result.append((f + word, f + yomi))
    # word + suffix
    for s in suffixes:
        result.append((word + s, yomi + s))
    # filler + word + suffix (top combos)
    for f in ["あの", "えー"]:
        for s in ["です", "なんですが", "ね"]:
            result.append((f + word + s, f + yomi + s))
    return result


def head_cuts(word, yomi, skip_1char=True):
    """Generate head-cut variants (drop 1-2 mora from front)."""
    result = []
    # mora approximation: each hiragana char = 1 mora
    if len(yomi) >= 3:
        cut1 = (word, yomi[1:])
        if not skip_1char or len(yomi[1:]) > 1:
            result.append(cut1)
    if len(yomi) >= 4:
        cut2 = (word, yomi[2:])
        if not skip_1char or len(yomi[2:]) > 1:
            result.append(cut2)
    return result


def dedup(pairs):
    seen = set()
    out = []
    for p in pairs:
        if p not in seen:
            seen.add(p)
            out.append(p)
    return out


