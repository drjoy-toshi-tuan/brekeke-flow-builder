# Helper to build profile_words string from list of (word, yomi)
# ============================================================

def to_pw(pairs):
    """Convert list of (word, yomi) to profile_words string."""
    seen = set()
    lines = []
    for word, yomi in pairs:
        key = f"{word} {yomi}"
        if key not in seen:
            seen.add(key)
            lines.append(key)
    return "\n".join(lines)


def count_lines(pw_str):
    return len([l for l in pw_str.split("\n") if l.strip()])


