# 8. 入力_緊急対応確認 (STT, yes_no → expand)
# TTS: "本件は、緊急対応が必要でしょうか。はい、いいえでお答えください。どうぞ。"
# ============================================================

def gen_kinkyuu():
    """YES/NO type: 緊急対応確認。肯定/否定 + 緊急文脈。"""
    pairs = []

    # フィラー TOP6
    fillers = ["あ", "え", "えー", "あの", "はい", "ま"]
    # 語尾 TOP8
    suffixes = ["です", "で", "なんですが", "になります", "ね", "さ", "でして", "か"]

    # 肯定系キーワード
    positive = [
        ("はい", "はい"),
        ("はい", "はあ"),       # 頭切れ/誤認識
        ("はい", "あい"),       # 頭切れ
        ("はい", "い"),         # 頭切れ（最重要）
        ("ええ", "ええ"),
        ("うん", "うん"),
        ("そうです", "そうです"),
        ("そうです", "おうです"),  # 頭切れ
        ("そうです", "うです"),    # 頭切れ
        ("大丈夫です", "だいじょうぶです"),
        ("大丈夫です", "いじょうぶです"),  # 頭切れ
        ("大丈夫", "だいじょうぶ"),
        ("大丈夫", "じょうぶ"),      # 頭切れ
        ("必要です", "ひつようです"),
        ("必要", "ひつよう"),
        ("緊急です", "きんきゅうです"),
        ("緊急", "きんきゅう"),
        ("お願いします", "おねがいします"),
        ("おねがいします", "ねがいします"),  # 頭切れ
        ("よろしいです", "よろしいです"),
        ("そうですね", "そうですね"),
        ("あります", "あります"),
        ("あり", "あり"),
        ("はーい", "はーい"),
    ]
    pairs.extend(positive)

    # 否定系キーワード
    negative = [
        ("いいえ", "いいえ"),
        ("いいえ", "いい"),      # 頭切れ
        ("いや", "いや"),
        ("違います", "ちがいます"),
        ("ちがいます", "がいます"),  # 頭切れ
        ("違う", "ちがう"),
        ("間違い", "まちがい"),
        ("まちがいます", "ちがいます"),
        ("必要ありません", "ひつようありません"),
        ("必要ない", "ひつようない"),
        ("ないです", "ないです"),
        ("ありません", "ありません"),
        ("違います", "がいます"),   # 頭切れ（再掲念のため）
        ("いいえ", "えー"),         # 誤認識パターン
        ("ちょっと", "ちょっと"),   # 「ちょっと違います」等
    ]
    pairs.extend(negative)

    # フィラー + 肯定/否定
    main_pos = [("はい", "はい"), ("そうです", "そうです"), ("必要です", "ひつようです")]
    main_neg = [("いいえ", "いいえ"), ("違います", "ちがいます"), ("必要ありません", "ひつようありません")]
    for f in fillers:
        for word, yomi in main_pos + main_neg:
            pairs.append((word, f + yomi))

    # 語尾パターン
    key_kw = [
        ("そうです", "そうです"),
        ("必要", "ひつよう"),
        ("緊急", "きんきゅう"),
        ("大丈夫", "だいじょうぶ"),
    ]
    for suffix in suffixes:
        for word, yomi in key_kw:
            pairs.append((word, yomi + suffix))

    # 緊急特有の頭切れ
    clips = [
        ("緊急", "んきゅう"),     # き → drop
        ("必要", "つよう"),       # ひ → drop
        ("必要", "よう"),         # ひつ → drop
        ("ありません", "りません"),
        ("あります", "ります"),
        ("大丈夫", "いじょうぶ"),  # 再掲
        ("必要です", "つようです"),
        ("はーい", "ーい"),
    ]
    pairs.extend(clips)

    pw = to_pw(pairs)
    print(f"入力_緊急対応確認: {count_lines(pw)} words")
    return pw


