import json, sys
sys.stdout.reconfigure(encoding='utf-8')

FILLERS = ['あ', 'え', 'えー', 'あの', 'はい', 'ま']
SUFFIXES = ['です', 'で', 'なんですが', 'になります', 'ね', 'さ', 'でして', 'か']

def generate_entries(display, reading, head_cuts=None):
    entries = []
    entries.append(f"{display} {reading}")
    for f in FILLERS:
        entries.append(f"{display} {f}{reading}")
    for s in SUFFIXES:
        entries.append(f"{display} {reading}{s}")
    if head_cuts:
        for hc_display, hc_reading in head_cuts:
            entries.append(f"{hc_display} {hc_reading}")
    return entries

def format_module(name, mod_type, tts_prompt, base_keywords, entries):
    global gen_counts_map
    lines = []
    lines.append(f"=== {name} (タイプ: {mod_type}) ===")
    lines.append(f"直前TTS: 「{tts_prompt}」")
    kw_names = [k[0] for k in base_keywords]
    lines.append(f"ベースキーワード: [{', '.join(kw_names)}]")
    num = len(entries)
    lines.append(f"生成語数: {num}")
    lines.append("")
    lines.append("[profile_words全文]")
    for e in entries:
        lines.append(e)
    lines.append("")
    gen_counts_map[name] = num
    return lines, num

