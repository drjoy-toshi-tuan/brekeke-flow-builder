# -*- coding: utf-8 -*-
"""go_back_classifier オラクル（L0 戻る/繰り返し検知）。

商談デモ「フリー発話受付」の各聴取STTの入力ゲートで、L1（答え取得判定）が ABSENT の
ときに走る L0。入力STTテキストを検査し、
  戻る     … 「やっぱり変更したい/キャンセルしたい/別の用件/最初から/やり直し」等＝前工程へ戻したい
  繰り返し … 「もう一回言って/もう一度/聞こえなかった/なんて言った」等＝直前のTTSを再生してほしい
  NONE     … いずれでもない（＝FAQ へ）
を返す。

呼び出し側（分岐_L0_X CMR）: 戻る→用件フリー聴取（intake再実行・既定）/ 繰り返し→聴取_X（当該TTS再生）/
NONE(^0$ other)→FAQ_X。

注意: L0 は L1=ABSENT（当該フィールドの答えが取れていない）後にのみ走るため、入力は既に
「フィールド回答ではない発話」に限定される。戻る/繰り返しの語彙はこの前提で安全側に絞る。

JS テンプレート docs/brekeke/script_templates/go_back_classifier.js と同一手順・同一辞書（parity）。
Note 非依存（語彙はコード内蔵）。
"""

# 戻る（前工程へ戻す・気が変わった）。強い意図語を優先。
GO_BACK_PHRASES = [
    "やっぱり", "やはり", "別の用件", "別件", "ほかの用件", "他の用件",
    "最初から", "最初に戻", "はじめから", "一から", "やり直し", "やりなおし",
    "前に戻", "戻して", "戻りたい", "違う用件",
    "取り消して", "取消して",
    # 注: "変更したい" / "キャンセルしたい" は用件発話でも使われるため除外。
    # "やっぱりキャンセル" 等は "やっぱり" で拾われる。
]

# 繰り返し（直前のTTSをもう一度）。
REPEAT_PHRASES = [
    "もう一回", "もう一度", "もういちど", "もっかい", "もう１回",
    "聞こえなかった", "聞こえません", "聞こえなくて", "聞こえづらい", "聞き取れ",
    "なんて言った", "なんて言いました", "なんと言った", "何て言った",
    "言ってください", "言って下さい", "もう一度言って", "繰り返し", "くりかえし",
]


def _has_any(s, arr):
    for p in arr:
        if p in s:
            return True
    return False


def classify(raw):
    """戻る / 繰り返し / NONE を返す。"""
    if raw is None:
        return "NONE"
    s = str(raw).strip()
    if s == "":
        return "NONE"
    # 戻る（気が変わった系）を優先（「もう一回最初から」等の複合は戻る意図）
    if _has_any(s, GO_BACK_PHRASES):
        return "戻る"
    if _has_any(s, REPEAT_PHRASES):
        return "繰り返し"
    return "NONE"
