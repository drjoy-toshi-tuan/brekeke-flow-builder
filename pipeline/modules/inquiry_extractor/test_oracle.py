def main():
    if hasattr(sys.stdout, "reconfigure"):
        sys.stdout.reconfigure(encoding="utf-8")
    passed = 0
    fails = []
    for cid, raw, exp_intent, exp_vals in CASES:
        r = parse(raw)
        errs = []
        if r["用件種別"] != exp_intent:
            errs.append(f"用件種別 期待={exp_intent!r} 実際={r['用件種別']!r}")
        for slot in SLOTS:
            want = exp_vals.get(slot, "")
            got = r["values"][slot]
            if got != want:
                errs.append(f"{slot} 期待={want!r} 実際={got!r}")
        if errs:
            fails.append((cid, errs))
        else:
            passed += 1
    print(f"用件抽出オラクル v2 受入テスト: {passed}/{len(CASES)} PASS")
    if fails:
        print("--- FAIL ---")
        for cid, errs in fails:
            print(f"  [{cid}]")
            for e in errs:
                print(f"      {e}")
        sys.exit(1)
    print("ALL PASS")


if __name__ == "__main__":
    main()
